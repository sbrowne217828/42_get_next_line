#include "libft.h"

void	*ft_memmove(void *s1, const void *s2, size_t n)
{
	char	*d;
	char	*s;
	size_t	i;

	d = (char*)s1;
	s = (char*)s2;
	i = 0;
	if (s < d)
	{
		while (n--)
			d[n] = s[n];
	}
	else
		ft_memcpy(d, s, n);
	return (d);
}
